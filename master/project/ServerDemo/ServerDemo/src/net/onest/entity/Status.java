package net.onest.entity;

public class Status {
	private boolean status;
	

	public Status() {
		super();
	}


	public Status(Boolean status) {
		super();
		this.status = status;
	}


	public boolean getStatus() {
		return status;
	}


	public void setStatus(boolean status) {
		this.status = status;
	}
	
	
	

}
